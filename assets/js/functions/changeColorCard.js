function changeColorAccordingToType(type) {
  switch (type) {
    case "normal":
      return "#75515B";
      break;
    case "fighting":
      return "#994025";
      break;
    case "flying":
      return "#4A677D";
      break;
    case "poison":
      return "#5E2D88";
      break;
    case "ground":
      return "#A9702C";
      break;
    case "rock":
      return "#48180B";
      break;
    case "bug":
      return "#1C4B27";
      break;
    case "ghost":
      return "#33336B";
      break;
    case "water":
      return "#1552E2";
      break;
    case "steel":
      return "#5F756D";
      break;
    case "fire":
      return "#AB1F23";
      break;
    case "grass":
      return "#147B3D";
      break;
    case "electric":
      return "#a5a722";
      break;
    case "psychic":
      return "#A32B6C";
      break;
    case "dragon":
      return "#448B95";
      break;
    case "dark":
      return "#040706";
      break;
    case "fairy":
      return "#951A44";
      break;
    case "ice":
      return "#86D2F5";
      break;
  }
}
